class Calculadora {

    companion object{
        fun soma(num1: Double, num2: Double): Double{
            return num1 + num2
        }

        fun soma(num1: Int, num2: Int): Int{
            return num1 + num2
        }

        fun subtracao(num1: Double, num2: Double): Double{
            return num1 - num2
        }
    }


}